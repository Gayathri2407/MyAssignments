public class CheckNumberIsPositive {

	static int i = 10;
	static int j = -10;
	
	public static void main(String[] args) {
		if(i>=0) {
			System.out.println(i+" is a Positive Integer");
		}
		if(j<0){
			System.out.println(j+ " is a Negative Integer");
		}
		
	}

	
}
